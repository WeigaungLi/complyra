def utcnow_naive() -> datetime:
    """Return the current UTC time without timezone info.

    This is a helper used to stamp "updated_at" and "decided_at" fields
    when modifying records. It matches the same function in models.py.
    """
    return datetime.now(timezone.utc).replace(tzinfo=None)


