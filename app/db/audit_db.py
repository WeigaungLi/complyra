# ── User-Tenant Assignment Operations ─────────────────────────────────

def assign_user_tenant(*, user_id: str, tenant_id: str) -> UserTenant:
    """Grant a user access to a tenant. If already assigned, return the existing link.

    This is idempotent — calling it twice with the same user and tenant
    has no side effect. It first checks if the link already exists to
    avoid creating duplicates.
    """
    with SessionLocal() as db:
        # Check if this user-tenant link already exists
        existing = db.execute(
            select(UserTenant).where(and_(UserTenant.user_id == user_id, UserTenant.tenant_id == tenant_id))
        ).scalar_one_or_none()
        if existing:
            return existing
        # Create a new link between the user and the tenant
        link = UserTenant(user_id=user_id, tenant_id=tenant_id)
        db.add(link)
        db.commit()
        db.refresh(link)
        return link


def list_user_tenants(user_id: str) -> list[UserTenant]:
    """Return all tenant links for a given user.

    This tells you which tenants (workspaces) a user has access to.
    """
    with SessionLocal() as db:
        return list(db.execute(select(UserTenant).where(UserTenant.user_id == user_id)).scalars())


def user_has_tenant(*, user_id: str, tenant_id: str) -> bool:
    """Check whether a user has access to a specific tenant.

    Returns True if the link exists, False otherwise. Used for
    authorization checks — ensuring a user can only access data
    in tenants they belong to.
    """
    with SessionLocal() as db:
        row = db.execute(
            select(UserTenant).where(and_(UserTenant.user_id == user_id, UserTenant.tenant_id == tenant_id))
        ).scalar_one_or_none()
        return row is not None


