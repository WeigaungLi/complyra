def update_approval(
    *,
    approval_id: str,
    status: str,
    decision_by: str,
    decision_note: str,
    final_answer: Optional[str],
) -> Optional[Approval]:
    """Record a reviewer's decision on an approval request.

    Sets the status to "approved" or "rejected", records who made the
    decision and when, and optionally stores an edited final answer.
    Returns None if the approval_id does not exist.
    """
    with SessionLocal() as db:
        approval = db.get(Approval, approval_id)
        if not approval:
            return None
        approval.status = status
        approval.decision_by = decision_by
        approval.decision_note = decision_note
        approval.decided_at = utcnow_naive()
        approval.final_answer = final_answer
        db.commit()
        db.refresh(approval)
        return approval


