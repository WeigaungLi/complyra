# ── Audit Log Operations ─────────────────────────────────────────────

def insert_log(
    *,
    tenant_id: str,
    user: str,
    action: str,
    input_text: str,
    output_text: str,
    metadata: str,
) -> None:
    """Write one entry to the audit log.

    This is called whenever something important happens (a question is asked,
    a document is uploaded, etc.). The log is append-only — entries are never
    modified or deleted, ensuring a reliable compliance trail.

    All parameters are keyword-only (the * in the signature) to prevent
    accidentally mixing up arguments.
    """
    with SessionLocal() as db:
        db.add(
            AuditLog(
                tenant_id=tenant_id,
                user=user,
                action=action,
                input_text=input_text,
                output_text=output_text,
                meta_json=metadata,
            )
        )
        db.commit()


def list_logs(*, tenant_ids: list[str], limit: int = 100) -> list[AuditLog]:
    """Fetch the most recent audit log entries for the given tenants.

    Returns up to 'limit' rows, sorted newest-first. The tenant_ids filter
    ensures users only see logs from tenants they have access to.
    """
    with SessionLocal() as db:
        # Build a query: SELECT * FROM audit_logs
        #   WHERE tenant_id IN (:tenant_ids)
        #   ORDER BY id DESC   (newest first)
        #   LIMIT :limit
        query = select(AuditLog).where(AuditLog.tenant_id.in_(tenant_ids)).order_by(AuditLog.id.desc()).limit(limit)
        return list(db.execute(query).scalars())


def search_logs(
    *,
    tenant_ids: list[str],
    username: Optional[str],
    action: Optional[str],
    start_time: Optional[datetime],
    end_time: Optional[datetime],
    limit: int,
) -> list[AuditLog]:
    """Search audit logs with optional filters for user, action, and time range.

    This builds the query dynamically — only the filters that are provided
    (not None) are added to the WHERE clause. This lets the caller search
    by any combination of criteria.
    """
    with SessionLocal() as db:
        # Start with: SELECT * FROM audit_logs WHERE tenant_id IN (...)
        query = select(AuditLog).where(AuditLog.tenant_id.in_(tenant_ids))
        # Optionally narrow down by specific user
        if username:
            query = query.where(AuditLog.user == username)
        # Optionally narrow down by action type (e.g., "query", "login")
        if action:
            query = query.where(AuditLog.action == action)
        # Optionally narrow down to entries after a start time
        if start_time:
            query = query.where(AuditLog.timestamp >= start_time)
        # Optionally narrow down to entries before an end time
        if end_time:
            query = query.where(AuditLog.timestamp <= end_time)
        # Always sort newest-first and cap the number of results
        query = query.order_by(AuditLog.id.desc()).limit(limit)
        return list(db.execute(query).scalars())


