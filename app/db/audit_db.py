def upsert_tenant_policy(
    *,
    tenant_id: str,
    approval_mode: str,
    updated_by: str,
) -> TenantPolicy:
    """Create or update the approval policy for a tenant.

    "Upsert" means: if a policy already exists for this tenant, update it;
    if not, create a new one. This avoids the need to check-then-create
    in the calling code.
    """
    with SessionLocal() as db:
        policy = db.get(TenantPolicy, tenant_id)
        if policy:
            # Policy exists — update it with the new values
            policy.approval_mode = approval_mode
            policy.updated_by = updated_by
            policy.updated_at = utcnow_naive()
        else:
            # No policy exists yet — create a new one
            policy = TenantPolicy(
                tenant_id=tenant_id,
                approval_mode=approval_mode,
                updated_by=updated_by,
            )
            db.add(policy)
        db.commit()
        db.refresh(policy)
        return policy


# ── Seed Data ──────────────────────────────────────────────────────────

def ensure_default_seed(*, demo_username: str, demo_password_hash: str, default_tenant_id: str) -> None:
    """Create default tenant and admin user if they don't already exist.

    This is called at application startup to ensure there is always at
    least one tenant and one admin user available for login. It is
    idempotent — running it multiple times has no side effect because
    it checks for existing records before creating new ones.

    The demo user is given the "admin" role and linked to the default tenant.
    """
    with SessionLocal() as db:
        # Create the default tenant if it doesn't exist yet
        tenant = db.get(Tenant, default_tenant_id)
        if not tenant:
            tenant = Tenant(tenant_id=default_tenant_id, name="Default Tenant")
            db.add(tenant)

        # Create the demo admin user if they don't exist yet
        user = db.execute(select(User).where(User.username == demo_username)).scalar_one_or_none()
        if not user:
            user_id = str(uuid4())
            user = User(
                user_id=user_id,
                username=demo_username,
                password_hash=demo_password_hash,
                role="admin",
                default_tenant_id=default_tenant_id,
            )
            db.add(user)
            # Also link the new user to the default tenant so they can access it
            db.add(UserTenant(user_id=user_id, tenant_id=default_tenant_id))

        db.commit()
