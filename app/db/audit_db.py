def list_documents_db(
    *,
    tenant_id: str,
    status: str | None = "active",
    sensitivity: str | None = None,
    limit: int = 100,
    offset: int = 0,
) -> tuple[list[Document], int]:
    """List documents for a tenant with optional filters and pagination.

    Returns a tuple of (documents, total_count). The total_count is the
    number of matching documents BEFORE applying limit/offset, which is
    needed by the UI to show "page 2 of 5" style pagination.

    By default, only "active" documents are returned (not archived/deleted).
    """
    with SessionLocal() as db:
        # Build the main query to fetch document rows
        query = select(Document).where(Document.tenant_id == tenant_id)
        # Build a parallel COUNT query to get the total number of matches
        # (needed for pagination — e.g., "showing 10 of 42 documents")
        count_query = select(func.count()).select_from(Document).where(Document.tenant_id == tenant_id)
        # Apply optional status filter to both queries
        if status:
            query = query.where(Document.status == status)
            count_query = count_query.where(Document.status == status)
        # Apply optional sensitivity filter to both queries
        if sensitivity:
            query = query.where(Document.sensitivity == sensitivity)
            count_query = count_query.where(Document.sensitivity == sensitivity)
        # Execute the count query first to get the total
        total = db.execute(count_query).scalar() or 0
        # Then fetch the actual page of results, sorted newest-first
        query = query.order_by(Document.created_at.desc()).offset(offset).limit(limit)
        docs = list(db.execute(query).scalars())
        return docs, total


def update_document_db(
    *,
    document_id: str,
    sensitivity: str | None = None,
    status: str | None = None,
    approval_override: str | None = "__unset__",
) -> Document | None:
    """Update a document's sensitivity, status, or approval override.

    Returns the updated document, or None if the document_id was not found.

    The approval_override parameter uses a special sentinel value "__unset__"
    to distinguish between two different intentions:
      - approval_override="__unset__" means "don't change this field at all"
        (the caller didn't provide a value).
      - approval_override=None means "clear this field" (set it to NULL in
        the database, removing any per-document override).
      - approval_override="always" or "none" means "set this specific value".

    We can't use None alone for "don't change" because None is a valid value
    we might want to SET (to clear the override). The "__unset__" string
    solves this ambiguity.
    """
    with SessionLocal() as db:
        doc = db.get(Document, document_id)
        if not doc:
            return None
        if sensitivity is not None:
            doc.sensitivity = sensitivity
        if status is not None:
            doc.status = status
        # Only update approval_override if the caller explicitly provided a value
        # (i.e., it's not the "__unset__" sentinel)
        if approval_override != "__unset__":
            doc.approval_override = approval_override
        doc.updated_at = utcnow_naive()
        db.commit()
        db.refresh(doc)
        return doc


def bulk_update_documents_db(
    *,
    document_ids: list[str],
    tenant_id: str,
    sensitivity: str | None = None,
    status: str | None = None,
) -> int:
    """Update multiple documents at once (batch operation).

    Only updates documents that match BOTH the given IDs AND the tenant_id.
    The tenant_id check is a security measure — it prevents a user from
    modifying documents that belong to a different tenant, even if they
    somehow know the document IDs.

    Returns the number of documents that were actually updated.
    """
    with SessionLocal() as db:
        # Find all documents matching the given IDs AND belonging to this tenant
        query = select(Document).where(
            and_(Document.document_id.in_(document_ids), Document.tenant_id == tenant_id)
        )
        docs = list(db.execute(query).scalars())
        # Apply the requested changes to each matching document
        for doc in docs:
            if sensitivity is not None:
                doc.sensitivity = sensitivity
            if status is not None:
                doc.status = status
            doc.updated_at = utcnow_naive()
        db.commit()
        return len(docs)


def get_documents_by_ids(document_ids: list[str]) -> list[Document]:
    """Fetch multiple documents by their IDs in a single query.

    Returns an empty list if no IDs are provided (avoids an unnecessary
    database call). Used when you already have a list of document IDs
    (e.g., from search results) and need the full document details.
    """
    if not document_ids:
        return []
    with SessionLocal() as db:
        query = select(Document).where(Document.document_id.in_(document_ids))
        return list(db.execute(query).scalars())


