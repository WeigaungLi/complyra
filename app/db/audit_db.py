"""
Database Read/Write Operations (CRUD Layer)

This file contains all the functions that read from and write to the database.
CRUD stands for Create, Read, Update, Delete — the four basic operations you
can do with stored data.

Instead of writing raw SQL queries, these functions use SQLAlchemy (a Python
library) to build queries in Python code. Each function opens a short-lived
database session (a connection), performs the operation, and then closes it.

Key patterns used throughout this file:

1. SessionLocal() — Opens a new database connection. Used inside a "with"
   block so the connection is automatically closed when done, even if an
   error occurs.

2. db.add(obj) + db.commit() — Inserts a new row (add) and saves the change
   permanently (commit). If something goes wrong, the "with" block ensures
   the session is rolled back (undone) automatically.

3. db.refresh(obj) — After committing, re-reads the object from the database
   to pick up any values that were generated by the database itself (like
   auto-incrementing IDs or default timestamps).

4. db.get(Model, id) — Fetches a single row by its primary key. Returns
   None if no matching row exists.

5. select(Model).where(...) — Builds a SQL SELECT query with filters.
   .scalars() converts the raw database rows into Python objects.
"""

from __future__ import annotations

from datetime import datetime, timezone
from typing import Optional
from uuid import uuid4

from sqlalchemy import and_, func, select

from app.db.models import Approval, AuditLog, Document, IngestJob, Tenant, TenantPolicy, User, UserTenant
